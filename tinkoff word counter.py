# Определяем функцию countWord, которая принимает строку string в качестве аргумента
def countWord(string):
    # Определяем слово "Tinkoff" в нижнем регистре
    word = "Tinkoff".lower()

    # Удаляем пробелы из входной строки и переводим ее в нижний регистр
    string = string.replace(" ", "").lower()

    # Инициализируем переменную count, которая будет хранить минимальное количество раз, которое можно собрать слово
    count = int(1e9)  # используем большое целое число вместо inf

    # Итерируемся по уникальным символам слова
    for char in set(word):
        # Для каждого символа считаем, сколько раз он появляется в строке и делим на количество его появлений в слове
        char_count_in_string = string.count(char)
        char_count_in_word = word.count(char)

        # Если количество символа в строке меньше, чем в слове, возвращаем 0
        if char_count_in_string < char_count_in_word:
            return 0

        # Берем минимальное значение между текущим count и рассчитанным значением
        count = min(count, char_count_in_string // char_count_in_word)

    # Возвращаем значение count, которое представляет максимальное количество раз, которое можно собрать слово, используя каждый символ только один раз
    return count

# Получаем входную строку от пользователя
string = input()

# Выводим количество раз, которое можно собрать слово
print(countWord(string))